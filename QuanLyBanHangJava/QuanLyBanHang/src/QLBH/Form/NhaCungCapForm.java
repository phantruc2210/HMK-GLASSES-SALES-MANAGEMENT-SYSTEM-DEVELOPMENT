    public void load()
    {
        try
        {
            tblNCC.removeAll();
            String[] arr = {"Mã NCC", "Tên NCC", "Địa chỉ", "Điện thoại", "Email"};
            tbtModel = new DefaultTableModel(arr, 0);
            
            String sql = "SELECT * FROM NhaCungCap";
            Connection conn = DatabaseConnection.openConnect();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            
            tbtModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MaNCC"), rs.getString("TenNCC"), rs.getString("DiaChi"),
                    rs.getString("DienThoai"), rs.getString("Email")};
                tbtModel.addRow(row);
            }
            tblNCC.setModel(tbtModel);
            tbtModel.fireTableDataChanged();
            DatabaseConnection.CloseConnect(conn);
        }
        catch (Exception e) 
        {
            e.printStackTrace();
        }
    }
