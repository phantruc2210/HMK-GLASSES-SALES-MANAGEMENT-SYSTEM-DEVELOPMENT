    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtMaNCC.getText().equals("")) 
        {
            sb.append("Mã nhà cung cấp không được để trống!\n");
            txtMaNCC.requestFocus();
        }
        if (sb.length() > 0) 
        {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            NhaCungCap ncc = new NhaCungCap();
            ncc.setMaNCC(txtMaNCC.getText());
            ncc.setTenNCC(txtTenNCC.getText());
            ncc.setDiaChi(txtDiaChi.getText());
            ncc.setDienThoai(txtDienThoai.getText());
            ncc.setEmail(txtEmail.getText());
            
            NhaCungCapDao nccd = new NhaCungCapDao();
            nccd.insert(ncc);
            load();
           
            JOptionPane.showMessageDialog(this, "Hoàn tất thêm nhà cung cấp!");
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this, "Lỗi. Không thể thêm nhà cung cấp!");
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemActionPerformed

