    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtMaPB.getText().equals("")) 
        {
            sb.append("Mã phòng ban không được để trống!\n");
            txtMaPB.requestFocus();
        }
        if (sb.length() > 0) 
        {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            PhongBan pb = new PhongBan();
            pb.setMaPB(txtMaPB.getText());
            pb.setTenPB(txtTenPB.getText());
            pb.setGhiChu(txtGhiChu.getText());
            
            PhongBanDao pbd = new PhongBanDao();
            pbd.insert(pb);
            load();
           
            JOptionPane.showMessageDialog(this, "Hoàn tất thêm phòng ban!");
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this, "Lỗi. Không thể thêm phòng ban!");
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemActionPerformed

