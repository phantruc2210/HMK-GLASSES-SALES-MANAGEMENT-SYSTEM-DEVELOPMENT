    private void initCombobox1() {
        try {
            String sql = "Select MaloaiSP from LoaiSanPham";
            Connection conn = DatabaseConnection.openConnect();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            cboxMaloaiSP.removeAllItems();
            while (rs.next()) {
                cboxMaloaiSP.addItem(rs.getString("MaloaiSP"));
            }
            rs.close();
            pm.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }
    
    public void load()
    {
        try
        {
            tblSP.removeAll();
            String[] arr = {"Mã SP", "Tên SP", "Số lượng tồn", "Đơn vị tính", "Mã loại SP"};
            tbtModel = new DefaultTableModel(arr, 0);
            
            String sql = "SELECT * FROM SanPham";
            Connection conn = DatabaseConnection.openConnect();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            
            tbtModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MaSP"), rs.getString("TenSP"), rs.getString("SLTon"),
                    rs.getString("DonViTinh"), rs.getString("MaloaiSP")};
                tbtModel.addRow(row);
            }
            tblSP.setModel(tbtModel);
            tbtModel.fireTableDataChanged();
            DatabaseConnection.CloseConnect(conn);
        }
        catch (Exception e) 
        {
            e.printStackTrace();
        }
    }

