    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtMaSP.getText().equals("")) 
        {
            sb.append("Mã sản phẩm không được để trống!\n");
            txtMaSP.requestFocus();
        }
        if (sb.length() > 0) 
        {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            SanPham sp = new SanPham();
            sp.setMaSP(txtMaSP.getText());
            sp.setTenSP(txtTenSP.getText());
            sp.setSLTon(Integer.parseInt(txtSLTon.getText()));
            sp.setDonViTinh(txtDonViTinh.getText());
            String n1 = cboxMaloaiSP.getItemAt(cboxMaloaiSP.getSelectedIndex());
            sp.setMaloaiSP(n1);
            
            
            SanPhamDao spd = new SanPhamDao();
            spd.insert(sp);
            load();
           
            JOptionPane.showMessageDialog(this, "Hoàn tất thêm sản phẩm!");
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this, "Lỗi. Không thể thêm sản phẩm!");
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemActionPerformed

