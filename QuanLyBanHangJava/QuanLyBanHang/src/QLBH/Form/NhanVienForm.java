    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtMaNV.getText().equals("")) 
        {
            sb.append("Mã nhân viên không được để trống!\n");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int dk = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa nhân viên này?", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (dk == JOptionPane.YES_OPTION) {
            try {
                NhanVienDao nv = new NhanVienDao();
                if (nv.Delete(txtMaNV.getText())) 
                {
                    JOptionPane.showMessageDialog(this, "Hoàn tất xóa nhân viên.");
                } else 
                {
                    JOptionPane.showMessageDialog(this, "Lỗi. Không thể xóa nhân viên.");
                }
                load();
            } catch (Exception e) 
            {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

