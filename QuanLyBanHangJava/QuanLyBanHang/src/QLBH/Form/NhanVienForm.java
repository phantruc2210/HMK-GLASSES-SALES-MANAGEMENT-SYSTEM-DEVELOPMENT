    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtMaNV.getText().equals("")) 
        {
            sb.append("Mã nhân viên không được để trống!\n");
            txtMaNV.requestFocus();
        }
        if (sb.length() > 0) 
        {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            NhanVien nv = new NhanVien();
            nv.setMaNV(txtMaNV.getText());
            nv.setHoNV(txtHoNV.getText());
            nv.setTenNV(txtTenNV.getText());
            
            String n2 = cboxGioiTinh.getItemAt(cboxGioiTinh.getSelectedIndex());
            nv.setGioiTinh(n2);
            
            nv.setNgaySinh(txtNgaySinh.getText());
            nv.setDiaChi(txtDiaChi.getText());
            nv.setDienThoai(txtDienThoai.getText());
            nv.setNoiSinh(txtNoiSinh.getText());
            nv.setNgayVaoLam(txtNgayVaoLam.getText());
            nv.setEmail(txtEmail.getText());
            String n1 = cboxMaPB.getItemAt(cboxMaPB.getSelectedIndex());
            nv.setMaPB(n1);
            
            NhanVienDao nvd = new NhanVienDao();
            nvd.insert(nv);
            load();
           
            JOptionPane.showMessageDialog(this, "Hoàn tất thêm nhân viên!");
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this, "Lỗi. Không thể thêm nhân viên!");
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
                   
    }//GEN-LAST:event_btnThemActionPerformed

