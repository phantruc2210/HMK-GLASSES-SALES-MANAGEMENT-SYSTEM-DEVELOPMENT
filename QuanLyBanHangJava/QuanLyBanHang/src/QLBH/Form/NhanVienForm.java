    public void load() 
    {
        try
        {
            tblNV.removeAll();
            String[] arr = {"Mã NV", "Họ NV", "Tên NV", "Giới Tính",
                "Ngày Sinh", "Địa Chỉ", "Điện Thoại","Nơi Sinh", "Ngày Vào Làm", "Email", "Mã PB"};
            tbtModel = new DefaultTableModel(arr, 0);
            
            String sql = "SELECT * FROM NhanVien";
            Connection conn = DatabaseConnection.openConnect();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            
            tbtModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MaNV"), rs.getString("HoNV"), rs.getString("TenNV"),rs.getString("GioiTinh"),
                    rs.getString("NgaySinh"), rs.getString("DiaChi"), rs.getString("DienThoai"), rs.getString("NoiSinh"),
                    rs.getString("NgayVaoLam"), rs.getString("Email"), rs.getString("MaPB")};
                tbtModel.addRow(row);
            }
            tblNV.setModel(tbtModel);
            tbtModel.fireTableDataChanged();
            DatabaseConnection.CloseConnect(conn);
        }
        catch (Exception e) 
        {
            e.printStackTrace();
        }
    }

