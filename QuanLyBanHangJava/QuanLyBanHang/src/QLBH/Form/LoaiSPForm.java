    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
         StringBuilder sb = new StringBuilder();
        if (txtMaloaiSP.getText().equals("")) 
        {
            sb.append("Mã loại sản phẩm không được để trống!\n");
            txtMaloaiSP.requestFocus();
        }
        if (sb.length() > 0) 
        {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            LoaiSP lsp = new LoaiSP();
            lsp.setMaloaiSP(txtMaloaiSP.getText());
            lsp.setTenloaiSP(txtTenloaiSP.getText());
            lsp.setGhiChu(txtGhiChu.getText());
            
            LoaiSPDao lspd = new LoaiSPDao();
            lspd.insert(lsp);
            load();
           
            JOptionPane.showMessageDialog(this, "Hoàn tất thêm loại sản phẩm!");
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this, "Lỗi. Không thể thêm loại sản phẩm!");
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemActionPerformed

