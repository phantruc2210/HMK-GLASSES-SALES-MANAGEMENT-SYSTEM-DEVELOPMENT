    public void TongDT()
    {
        DecimalFormat x = new DecimalFormat("###,###,###");
        double tong = 0;
        for(int i=0; i< tblTK.getRowCount(); i++)
        {
            double a = Double.parseDouble(tblTK.getValueAt(i, 7).toString());
            double b = Double.parseDouble(tblTK.getValueAt(i, 6).toString());
            tong = tong + (a*b);
        }
        txtTongTien.setText(""+ x.format(tong) + " VND");
    }
    
    
  

