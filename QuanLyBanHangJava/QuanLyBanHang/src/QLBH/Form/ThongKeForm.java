    private void btnThongKeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThongKeActionPerformed
        // TODO add your handling code here:
        
        try
         {
           tblTK.removeAll();
            String[] arr = {"Mã HD", "Tên NV","Mã SP", "Tên SP","Ngày DH", "Tên KH", "Số lượng đặt", "Đơn giá bán"};
            tableModel = new DefaultTableModel(arr, 0);
            String sql = "SELECT cthd.MaHD, TenNV, cthd.MaSP, TenSP, NgayDH, TenKH, SoLuongDat, DGBan\n" +
                         "from HoaDon hd join CTHoaDon cthd on hd.MaHD = cthd.MaHD\n" +
                        "join NhanVien nv on hd.MaNV = nv.MaNV join\n" +
                        "KhachHang kh on kh.MaKH = hd.MaKH join\n" +
                        "SanPham sp on sp.MaSP = cthd.MaSP"  ;
                    
            Connection conn = DatabaseConnection.openConnect();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            tableModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MaHD"), 
                    rs.getString("TenNV"),rs.getString("MaSP"), rs.getString("TenSP"),
                    rs.getString("NgayDH"),rs.getString("TenKH"), rs.getString("SoLuongDat"),
                    rs.getString("DGBan")};
                tableModel.addRow(row);
                ThongKe tk = new ThongKe(rs.getString("MaHD"), 
                    rs.getString("TenNV"),rs.getString("MaSP"), rs.getString("TenSP"),
                    rs.getString("NgayDH"),rs.getString("TenKH"), Integer.valueOf(rs.getString("SoLuongDat")),
                    Double.valueOf(rs.getString("DGBan")));
                A.add(tk);
            }
            tblTK.setModel(tableModel);
            tableModel.fireTableDataChanged();
         
            TongDT();
           
         }
         catch (Exception e) 
         {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThongKeActionPerformed

