    public void load()
    {
        try
        {
            tblKH.removeAll();
            String[] arr = {"Mã KH", "Tên KH", "Địa Chỉ", "Ngày Sinh", "Điện Thoại"};
            tbtModel = new DefaultTableModel(arr, 0);
            
            String sql = "SELECT * FROM KhachHang";
            Connection conn = DatabaseConnection.openConnect();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            
            tbtModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MaKH"), rs.getString("TenKH"), rs.getString("DiaChi"),
                    rs.getString("NgaySinh"), rs.getString("DienThoai")};
                tbtModel.addRow(row);
            }
            tblKH.setModel(tbtModel);
            tbtModel.fireTableDataChanged();
            DatabaseConnection.CloseConnect(conn);
        }
        catch (Exception e) 
        {
            e.printStackTrace();
        }
    }

