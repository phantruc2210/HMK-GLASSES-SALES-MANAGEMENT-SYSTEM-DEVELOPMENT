    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtMaKH.getText().equals("")) 
        {
            sb.append("Mã khách hàng không được để trống!\n");
            txtMaKH.requestFocus();
        }
        if (sb.length() > 0) 
        {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            KhachHang kh = new KhachHang();
            kh.setMaKH(txtMaKH.getText());
            kh.setTenKH(txtTenKH.getText());
            kh.setDiaChi(txtDiaChi.getText());
            kh.setNgaySinh(txtNgaySinh.getText());
            kh.setDienThoai(txtDienThoai.getText());
            
            KhachHangDao khd = new KhachHangDao();
            khd.insert(kh);
            load();
           
            JOptionPane.showMessageDialog(this, "Hoàn tất thêm khách hàng!");
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this, "Lỗi. Không thể thêm khách hàng!");
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemActionPerformed

